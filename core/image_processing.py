import os
import subprocess
import shutil
import cv2

def generation_workflow_oemer(image_path: str, output_dir: str = "./") -> str:
    """
    I process the music sheet image and generate a MusicXML file using the Oemer Baseline engine.
    I also force the generation of diagnostic images and move everything to the output directory.
    I implement a safeguard: if the image is too large (> 3000px), I resize it to 2500px to avoid 
    crashing the RAM.
    """
    # --- MEMORY SAFEGUARD: IMAGE DOWNSCALING ---
    img = cv2.imread(image_path)
    if img is not None:
        h, w = img.shape[:2]
        max_dim = max(h, w)
        if max_dim > 3000:
            scale = 2500 / max_dim
            new_w = int(w * scale)
            new_h = int(h * scale)
            print(f"[Oemer Safeguard] 📏 Resizing large image from {w}x{h} to {new_w}x{new_h}...")
            img = cv2.resize(img, (new_w, new_h), interpolation=cv2.INTER_AREA)
            
            # Save downscaled version to a temporary path
            base_name = os.path.splitext(os.path.basename(image_path))[0]
            image_path = os.path.join(output_dir, f"{base_name}_downscaled.png")
            cv2.imwrite(image_path, img)

    # I extract the filename without extension to know what the XML will be called
    base_name = os.path.splitext(os.path.basename(image_path))[0]
    
    # The expected output XML name generated by oemer
    expected_xml = os.path.join(output_dir, f"{base_name}.musicxml")

    # I construct the exact command line string using the full, explicit flag names
    cmd = [
        "oemer", 
        image_path, 
        "-o", output_dir, 
        "--save-cache",     # I explicitly demand the diagnostic images
        "--without-deskew"  # I skip deskewing, just like in the original Python API call
    ]

    try:
        # I execute the command, waiting for it to finish and capturing any output/errors
        result = subprocess.run(cmd, check=True, capture_output=True, text=True)
    except subprocess.CalledProcessError as e:
        # If oemer crashes, I send its EXACT terminal error directly to the Streamlit interface
        raise RuntimeError(f"Oemer terminal crash:\n{e.stderr}\n{e.stdout}") from e

    # --- IMAGE RESCUE OPERATION ---
    # I look in the input directory, because oemer stubbornly saves cache images next to the input file
    input_dir = os.path.dirname(os.path.abspath(image_path))
    
    # These are the typical names oemer uses for its cache images
    cache_image_patterns = [
        "staffs.png", 
        "noteheads.png", 
        "clefs_keys.png", 
        "stems_rests.png",
        "bboxes.png"
    ]

    for pattern in cache_image_patterns:
        # I look for the file next to the input image
        source_path = os.path.join(input_dir, pattern)
        if os.path.exists(source_path):
            # If I find it, I securely move it to my testing output directory
            dest_path = os.path.join(output_dir, pattern)
            shutil.move(source_path, dest_path)

    # I verify that the XML file was actually created
    if not os.path.exists(expected_xml):
        # I check if oemer saved it in the input directory instead of the output directory
        fallback_xml = os.path.join(input_dir, f"{base_name}.musicxml")
        if os.path.exists(fallback_xml):
             shutil.move(fallback_xml, expected_xml)
        else:
            raise FileNotFoundError(f"I could not find the expected XML file: {expected_xml}")

    return expected_xml